moscow_tz = pytz.timezone("Europe/Moscow")

# –ó–∞–ø—Ä–æ—Å –∫ GPT
def ask_gpt_to_parse_task(text):
    system_prompt = (
        "–¢—ã –ø–æ–º–æ—â–Ω–∏–∫, –∫–æ—Ç–æ—Ä—ã–π –ø–æ–ª—É—á–∞–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ –¥–æ–ª–∂–µ–Ω —Ä–∞—Å–ø–æ–∑–Ω–∞—Ç—å –∑–∞–¥–∞—á—É. "
        "–û—Ç–≤–µ—Ç –≤–æ–∑–≤—Ä–∞—â–∞–π —Å—Ç—Ä–æ–≥–æ –≤ JSON —Å –ø–æ–ª—è–º–∏: title (—Å—Ç—Ä–æ–∫–∞), description (—Å—Ç—Ä–æ–∫–∞), "
        "due_date (—Å—Ç—Ä–æ–∫–∞ –≤ ISO 8601 –∏–ª–∏ null), labels (—Å–ø–∏—Å–æ–∫ —Å—Ç—Ä–æ–∫)."
    )
    response = openai.ChatCompletion.create(
        model="gpt-4",
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": text}
        ],
        temperature=0.2,
    )
    return response["choices"][0]["message"]["content"]

# –ü–∞—Ä—Å–∏–Ω–≥ –¥–∞—Ç—ã
def parse_due_date(text):
    now = datetime.now(moscow_tz)
    parsed_date = dateparser.parse(
        text,
        settings={
            "TIMEZONE": "Europe/Moscow",
            "TO_TIMEZONE": "Europe/Moscow",
            "PREFER_DATES_FROM": "future",
            "RETURN_AS_TIMEZONE_AWARE": True,
            "RELATIVE_BASE": now
        }
    )
    if parsed_date:
        print(f"[DEBUG] üìÖ –†–∞—Å–ø–æ–∑–Ω–∞–Ω–æ –∫–∞–∫: {parsed_date.isoformat()}")

        # –ï—Å–ª–∏ GPT –¥–∞–ª —Å—Ç–∞—Ä—É—é –¥–∞—Ç—É –±–µ–∑ —É–∫–∞–∑–∞–Ω–∏—è –≥–æ–¥–∞
        if parsed_date < now:
            parsed_date = parsed_date.replace(year=now.year + 1)
            print(f"[DEBUG] ‚ö†Ô∏è –î–∞—Ç–∞ –±—ã–ª–∞ –≤ –ø—Ä–æ—à–ª–æ–º, –∑–∞–º–µ–Ω–∏–ª–∏ –Ω–∞: {parsed_date.isoformat()}")
        return parsed_date.isoformat()
    print("[DEBUG] ‚ö†Ô∏è –î–∞—Ç–∞ –Ω–µ —Ä–∞—Å–ø–æ–∑–Ω–∞–Ω–∞")
    return None

# –û—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ Telegram
def send_message(chat_id, text):
    try:
        url = f"https://api.telegram.org/bot{TELEGRAM_BOT_TOKEN}/sendMessage"
        requests.post(url, json={"chat_id": chat_id, "text": text})
    except Exception as e:
        print(f"[ERROR] Telegram: {e}")

